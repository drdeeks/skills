def do_generate(args):
    target_fps = 60
    for i, arg in enumerate(args):
        if arg == "--target-fps" and i + 1 < len(args):
            target_fps = int(args[i + 1])

    code = PERF_TUNER_TEMPLATE.format(target_fps=target_fps)
    output_path = Path("src/systems/PerformanceTuner.ts")
    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text(code)

    stats = {
        "operation": "generate_performance_profiler",
        "timestamp": now_iso(),
        "status": "success",
        "config": {"target_fps": target_fps, "tiers": 5},
        "output": str(output_path),
        "cost": {"tier": 0, "amount_usd": 0.0, "service": "local"},
    }
    append_jsonl(ANALYTICS_DIR / "run_stats.jsonl", stats)
    print(f"Generated: {output_path}")
    print(json.dumps(stats, indent=2))


def do_status():
    stats_file = ANALYTICS_DIR / "run_stats.jsonl"
    if stats_file.exists():
        lines = stats_file.read_text().strip().split("\n")
        for line in lines[-5:]:
            print(json.dumps(json.loads(line), indent=2))
    else:
        print("No profiler runs recorded.")


COMMANDS = {
    "generate": do_generate,
    "status": lambda args: do_status(),
    "help": lambda args: print(__doc__),
}


def main():
    cmd = sys.argv[1] if len(sys.argv) > 1 else "help"
    args = sys.argv[2:]
    if cmd in ("--help", "-h"):
        cmd = "help"
    if cmd == "--dry-run":
        print("[DRY RUN] Would generate performance tuner module")
        return
    if cmd in COMMANDS:
        COMMANDS[cmd](args)
    else:
        print(f"Unknown command: {cmd}")
        sys.exit(1)


if __name__ == "__main__":
    main()
