#!/usr/bin/env python3
"""
AudioSurf Visualizer — Performance Profiler & Adaptive Quality Generator

Generates the adaptive performance tuning system that dynamically scales
visual quality based on real-time FPS to maintain smooth 60fps.

Usage:
    python3 performance_profiler.py generate [--target-fps 60]
    python3 performance_profiler.py benchmark
    python3 performance_profiler.py status
    python3 performance_profiler.py --help
    python3 performance_profiler.py --dry-run
"""

import json
import sys
from datetime import datetime, timezone
from pathlib import Path

STATE_DIR = Path.home() / ".audiosurf-visualizer"
ANALYTICS_DIR = STATE_DIR / "analytics"


def now_iso():
    return datetime.now(timezone.utc).isoformat()


def append_jsonl(filepath, record):
    Path(filepath).parent.mkdir(parents=True, exist_ok=True)
    with open(filepath, "a") as f:
        f.write(json.dumps(record) + "\n")


