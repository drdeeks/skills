#!/usr/bin/env python3
"""
skill-creator/__init__.py — Enterprise skill scaffolder + toolchain entry point.

Runs as either a Python module (`import skill_creator`) OR a CLI scaffolder
(`python3 __init__.py --scaffold my-new-skill --path /where/`). Per the rule
that __init__.py contains SCAFFOLDING for whatever the skill is responsible
for curating, and skill-creator's responsibility is creating skills, this
IS the scaffolder for new skills.

The scaffolded skeleton contains EXACTLY the 5 required root items:
  __init__.py       — scaffolding script for the new skill (uses this template)
  SKILL.md          — frontmatter + body template (contains REPLACE_ME_* markers)
  scripts/          — empty; author populates with real content
  references/       — empty; author populates with real content
  <name>.skill      — regenerated by package_skills.py; not created here

Nested `references/lessons/` is created only on demand (author's choice), and
does NOT affect enterprise tier classification.

NEVER creates `assets/`. Assets belong inside the specific script/reference
files that use them, not at the skill root.
"""
