def _slug(name):
    s = re.sub(r"[^a-z0-9-]+", "-", name.lower()).strip("-")
    return s[:64]


def _display(name):
    return name.replace("-", " ").title()


def _tag_lines(count):
    return "\n".join(
        f"    - REPLACE_ME_TAG_{i+1}_natural_language_phrase_that_would_auto_trigger_this_skill"
        for i in range(count)
    )


# ─── Public API ───────────────────────────────────────────────────────────

def scaffold(skill_name, base_dir, force=False, tier="enterprise",
             dry_run=False):
    """Create a new skill's root scaffold under base_dir.

    Args:
      skill_name: raw name — will be slugified to hyphen-case
      base_dir:   Path or str; the parent directory (e.g. .../curated/)
      force:      if dest exists, PRESERVE its SKILL.md/__init__.py by moving
                  them aside with a .pre-scaffold timestamp suffix (nothing is
                  ever deleted), then write fresh templates
      tier:       "enterprise" (default, 7 tags) — 'basic' is not accepted
                  because per project rule init only scaffolds enterprise-rated
      dry_run:    report what would be created without writing anything

    Returns dict with status + path + next-step guidance.
    """
    if tier != "enterprise":
        raise ValueError("scaffold() only creates enterprise-tier skills. "
                         "Use validate.py --basic to inspect existing basic skills; "
                         "never scaffold a basic-tier skeleton.")

    name = _slug(skill_name)
    if not name:
        raise ValueError(f"skill name {skill_name!r} slugs to empty")
    parent = Path(base_dir).resolve()
    if not parent.exists():
        raise FileNotFoundError(f"parent {parent} does not exist")

    dest = parent / name
    if dest.exists() and not force:
        raise FileExistsError(f"{dest} exists; pass force=True to re-scaffold "
                              "(existing files are moved aside, never deleted)")
    preserved = []
    if dest.exists() and force and not dry_run:
        # NEVER delete: move the files we'd regenerate aside, timestamped
        stamp = datetime.now(timezone.utc).strftime("%Y%m%d-%H%M%S")
        for fname in ("SKILL.md", "__init__.py"):
            existing = dest / fname
            if existing.is_file():
                aside = dest / f"{fname}.pre-scaffold-{stamp}"
                existing.rename(aside)
                preserved.append(str(aside))

    if dry_run:
        return {
            "operation": "scaffold",
            "skill": name,
            "path": str(dest),
            "tier": "enterprise",
            "dry_run": True,
            "would_create": ["SKILL.md", "__init__.py", "scripts/", "references/"],
            "would_preserve": [f"{f} → {f}.pre-scaffold-<ts>" for f in
                               ("SKILL.md", "__init__.py")
                               if (dest / f).is_file()] if dest.exists() else [],
        }

    dest.mkdir(parents=True, exist_ok=True)
    (dest / "scripts").mkdir(exist_ok=True)
    (dest / "references").mkdir(exist_ok=True)
    # NOTE: assets/ is intentionally NOT created. Nested references/lessons/
    # is created on-demand by the author, not here.

    # Write SKILL.md (7 tags because enterprise-only)
    (dest / "SKILL.md").write_text(SKILL_MD_TEMPLATE.format(
        name=name,
        display_name=_display(name),
        tag_lines=_tag_lines(7),
    ), encoding="utf-8")

    # Write child __init__.py (scaffolder-in-training for the new skill's own domain)
    (dest / "__init__.py").write_text(CHILD_INIT_TEMPLATE.format(
        name=name,
        timestamp=datetime.now(timezone.utc).isoformat(),
    ), encoding="utf-8")

    return {
        "operation": "scaffold",
        "skill": name,
        "path": str(dest),
        "tier": "enterprise",
        "preserved": preserved,
        "next_steps": [
            f"Edit {dest}/SKILL.md — fill every REPLACE_ME_* marker",
            f"Add ≥3 real scripts to {dest}/scripts/",
            f"Add ≥5 real reference docs to {dest}/references/",
            f"Fill scaffold() + integrate() bodies in {dest}/__init__.py "
            f"with logic specific to what this skill curates",
            f"Validate: python3 {Path(__file__).parent}/scripts/validate.py {dest}",
            f"Package:  python3 {Path(__file__).parent}/scripts/package_skills.py "
            f"--skills-root {parent} --skill {name}",
        ],
    }


LESSON_TEMPLATE = """---
title: {title}
category: REPLACE_ME_CATEGORY
failure: REPLACE_ME_CONCRETE_SYMPTOM_WHAT_BROKE_OR_SURPRISED
root_cause: REPLACE_ME_WHY_IT_HAPPENED
resolution: REPLACE_ME_WHAT_FIXED_IT
prevention: REPLACE_ME_THE_RULE_OR_PATTERN_GOING_FORWARD
date: {date}
verified: false
---

(optional prose elaboration below, only if the frontmatter fields alone don't
capture the full context)
"""


def convert(existing_dir, dry_run=False):
    """Non-destructively bring an EXISTING directory up to skill structure.

    Creates only what's missing (scripts/, references/, a minimal SKILL.md
    shell with REPLACE_ME markers). Never moves, renames, or deletes anything
    that's already there — run auto_fix.py + validate.py afterward for that.
    """
    d = Path(existing_dir).resolve()
    if not d.is_dir():
        raise FileNotFoundError(f"{d} is not a directory")
    name = _slug(d.name)
    created = []
    for sub in ("scripts", "references"):
        if not (d / sub).is_dir():
            if not dry_run:
                (d / sub).mkdir()
            created.append(f"{sub}/")
    if not (d / "SKILL.md").is_file():
        if not dry_run:
            (d / "SKILL.md").write_text(SKILL_MD_TEMPLATE.format(
                name=name, display_name=_display(name),
                tag_lines=_tag_lines(7)), encoding="utf-8")
        created.append("SKILL.md (template — fill every REPLACE_ME marker)")
    if not (d / "__init__.py").is_file():
        if not dry_run:
            (d / "__init__.py").write_text(CHILD_INIT_TEMPLATE.format(
                name=name,
                timestamp=datetime.now(timezone.utc).isoformat()),
                encoding="utf-8")
        created.append("__init__.py (template — fill scaffold()/integrate())")
    return {"operation": "convert", "path": str(d), "dry_run": dry_run,
            "created": created,
            "next": "populate templates, then run scripts/validate.py"}


def add_lesson(skill_dir, title, dry_run=False):
    """Add a structured lesson file to <skill>/references/lessons/."""
    d = Path(skill_dir).resolve()
    if not (d / "SKILL.md").is_file():
        raise FileNotFoundError(f"{d} has no SKILL.md — not a skill root")
    lessons = d / "references" / "lessons"
    fname = _slug(title) + ".md"
    dest = lessons / fname
    if dest.exists():
        raise FileExistsError(f"lesson already exists: {dest}")
    if not dry_run:
        lessons.mkdir(parents=True, exist_ok=True)
        dest.write_text(LESSON_TEMPLATE.format(
            title=title.strip(),
            date=datetime.now(timezone.utc).strftime("%Y-%m-%d")),
            encoding="utf-8")
    return {"operation": "add_lesson", "path": str(dest), "dry_run": dry_run}


def integrate(target_path, skill_name, dry_run=False):
    """Wire a scaffolded skill into an external project (import + register).

    Downstream skills override this for their specific integration story;
    skill-creator itself provides a generic 'copy + append import' fallback.
    """
    tgt = Path(target_path)
    if not tgt.exists():
        raise FileNotFoundError(f"target {tgt} does not exist")
    action = "would copy" if dry_run else "copied"
    return {"operation": "integrate", "skill": skill_name,
            "target": str(tgt), "action": action}


# ─── CLI ──────────────────────────────────────────────────────────────────

def _cli():
    ap = argparse.ArgumentParser(
        description="skill-creator — scaffold, convert, and integrate skills")
    ap.add_argument("--scaffold", help="Scaffold a new skill with this name")
    ap.add_argument("--path", help="Parent dir under which to create the skill")
    ap.add_argument("--convert", help="Existing dir to bring up to skill structure (creates only what's missing)")
    ap.add_argument("--add-lesson", help="Lesson title to add to a skill (use with --skill-dir)")
    ap.add_argument("--skill-dir", help="Skill dir (used with --add-lesson)")
    ap.add_argument("--integrate", help="Target project path to integrate into")
    ap.add_argument("--name", help="Skill name (used with --integrate)")
    ap.add_argument("--force", action="store_true",
                    help="Re-scaffold over existing (files moved aside, never deleted)")
    ap.add_argument("--dry-run", action="store_true", help="Preview without writing")
    args = ap.parse_args()
    if args.scaffold:
        if not args.path:
            sys.exit("ERROR: --path is required with --scaffold")
        r = scaffold(args.scaffold, args.path, force=args.force,
                     dry_run=args.dry_run)
        if r.get("dry_run"):
            print(f"[dry-run] would scaffold {r['skill']} at {r['path']}")
            for w in r.get("would_preserve", []):
                print(f"  would preserve: {w}")
        else:
            print(f"✓ scaffolded {r['skill']} at {r['path']} (enterprise-tier)")
            for p in r.get("preserved", []):
                print(f"  preserved existing: {p}")
            for s in r["next_steps"]:
                print(f"  → {s}")
    elif args.convert:
        r = convert(args.convert, dry_run=args.dry_run)
        prefix = "[dry-run] would create" if args.dry_run else "created"
        print(f"✓ convert {r['path']}")
        for c in r["created"]:
            print(f"  {prefix}: {c}")
        print(f"  → {r['next']}")
    elif args.add_lesson:
        if not args.skill_dir:
            sys.exit("ERROR: --skill-dir is required with --add-lesson")
        r = add_lesson(args.skill_dir, args.add_lesson, dry_run=args.dry_run)
        print(f"✓ {'[dry-run] would create' if args.dry_run else 'created'} "
              f"lesson: {r['path']}")
    elif args.integrate:
        r = integrate(args.integrate, args.name or "unknown", args.dry_run)
        print(r)
    else:
        ap.print_help()


if __name__ == "__main__":
    _cli()
